package ProiectEAP;

public class Vaucher {
    private int cod;
    private String descriere;

    public Vaucher(int cod, String descriere) {
        this.cod = cod;
        this.descriere = descriere;
    }

    public int getCod() {
        return cod;
    }

    public String getDescriere() {
        return descriere;
    }
}
